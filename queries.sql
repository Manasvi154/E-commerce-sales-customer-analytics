-- Time-based & Trends analysis
-- Calculate the number of orders per month in 2018
select year(order_purchase_timestamp) as order_year,
month(order_purchase_timestamp) as order_month,
count(*) as total_orders
from orders
where year(order_purchase_timestamp) = 2018
group by 
year(order_purchase_timestamp),
month(order_purchase_timestamp)
order by order_month asc;

-- Calculate the cumulative sales per month for each year
with monthly_sales as (
	select 
    year(o.order_purchase_timestamp) as order_year,
    month(o.order_purchase_timestamp) as order_month,
    sum(oi.price + oi.freight_value) as monthly_revenue
    from orders o 
    join order_items oi
    on o.order_id = oi.order_id
    group by 
    year(o.order_purchase_timestamp),
    month(o.order_purchase_timestamp)
)
select order_year, order_month, monthly_revenue,
sum(monthly_revenue) over(partition by order_year order by order_month) as cumulative_revenue
from monthly_sales
order by order_year, order_month;

-- Calculate the year-over-year(yoy) growth rate of total sales
with yearly_sales as (
	select 
    year(o.order_purchase_timestamp) as order_year,
    sum(oi.price + oi.freight_value) as total_revenue
    from orders o 
    join order_items oi
    on o.order_id = oi.order_id
    group by year(o.order_purchase_timestamp)
)
select order_year, total_revenue,
round((total_revenue - LAG(total_revenue) over (order by order_year)) * 100.0 / 
LAG(total_revenue) over (order by order_year), 2) as yoy_growth_percentage
from yearly_sales
order by order_year;

