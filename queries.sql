-- Advanced Ranking & Insights
-- Total revenue generated by each seller and rank them by revenue
with seller_revenue as (
select
s.seller_id, round(sum(oi.price + oi.freight_value), 2) as total_revenue
from order_items oi
join sellers s
on oi.seller_id = s.seller_id
group by s.seller_id
)
select
seller_id, total_revenue, rank() over(order by total_revenue desc) as revenue_rank
from seller_revenue
order by revenue_rank;

-- Identify the top 3 customers who spent the most money in each year
with yearly_customer_spend as (
select
year(o.order_purchase_timestamp) as order_year,
o.customer_id,
round(sum(oi.price + oi.freight_value), 2) as total_spent
from orders o
join order_items oi
on o.order_id = oi.order_id
group by
year (o.order_purchase_timestamp),
o.customer_id
),
ranked_customers as (
select
order_year,
customer_id,
total_spent,
DENSE_RANK() over (
partition by order_year
order by total_spent desc
) as customer_rank
from yearly_customer_spend
)
select
order_year,
customer_id,
total_spent
from ranked_customers
where customer_rank <= 3
order by order_year, total_spent desc;

