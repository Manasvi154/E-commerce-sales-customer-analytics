-- Revenue & sales analysis
-- Find the total sales per product category
select p.product_category_name, round(sum(oi.price + oi.freight_value), 2) as total_sales
from order_items as oi
join products p
on oi.product_id = p.product_id
group by p.product_category_name
order by total_sales desc;

-- Calculate the percentage of total revenue contributed by each category
select p.product_category_name,
round(sum(oi.price + oi.freight_value) * 100.0 / (select sum(price + freight_value) from order_items), 2) as revenue_percentage
from order_items as oi
join products p
on oi.product_id = p.product_id
group by p.product_category_name
order by revenue_percentage desc;

