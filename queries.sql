-- Customer Behavior analysis
-- Average number of products per order, grouped by customer city
select c.customer_city, round(count(oi.order_item_id) * 1.0 / count(distinct o.order_id), 2)
as avg_products_per_order
from order_items oi
join orders o
on oi.order_id = o.order_id
join customers c
on o.customer_id = c.customer_id
group by c.customer_city
order by avg_products_per_order desc;

-- Customer retention rate
-- % of customers who made another purchase within 6 months
with customer_orders as(
select
customer_id, min(order_purchase_timestamp) as first_order_date,
count(*) as total_orders
from orders
group by customer_id
)
select 
round(count(case when total_orders > 1 then 1 end) * 100.0 / count(*), 2)
as customer_retention_rate_percentage
from customer_orders;

